    private void deleteitemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteitemMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) receipt.getModel();
        receipt.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
        deleteitem.addActionListener((ActionEvent ae) -> {
            // check for selected row first
            if(receipt.getSelectedRow() != -1) {
                // remove selected row from the model
                String Value = (String) receipt.getValueAt(receipt.getSelectedRow() , receipt.getSelectedColumn());
                String[] prices = Value.split(" ");
                float price = Float.parseFloat(prices[0]);
                ttprice = ttprice - price;
                netprice.setText(String.valueOf(ttprice));
                model.removeRow(receipt.getSelectedRow());
                //JOptionPane.showMessageDialog(null, "Selected row deleted successfully");
            }
        });
       
    }//GEN-LAST:event_deleteitemMouseClicked

    private void checkdiscount()
    {
        if(cats[0] == 1 && cats [1] == 1 && cats[2] == 1)
        {
            int discount = 3;
            float finprice  = ttprice - discount;
            discountt.setText(String.valueOf(discount)+" £");
            finalprice.setText(String.valueOf(finprice)+" £");
        }else
        {
            discountt.setText(String.valueOf(0)+" £");
            finalprice.setText(String.valueOf(ttprice)+" £");
        }
    }
