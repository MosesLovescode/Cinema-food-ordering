public void tocsv(JTable table, File file){
    try{
        TableModel model = table.getModel();
        try (FileWriter csv = new FileWriter(file)) {
            for(int i = 0; i < model.getColumnCount(); i++){
                csv.write(model.getColumnName(i) + ",");
            }
            
            csv.write("\n");
            
            for(int i=0; i< model.getRowCount(); i++) {
                for(int j=0; j < model.getColumnCount(); j++) {
                    csv.write(model.getValueAt(i,j).toString()+",");
                }
                csv.write("\n");
            }
        }

    }catch(IOException e){ System.out.println(e); }
}

    private void loadcsvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loadcsvMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = fileChooser.getSelectedFile();
                System.out.println("Selected file: " + selectedFile.getAbsolutePath());
                Scanner fileScanner = new Scanner(selectedFile);
                DefaultTableModel model = (DefaultTableModel) datatable.getModel();
                String fileString = fileScanner.nextLine();
           
                while(fileScanner.hasNext()==true)
                {
                    fileString = fileScanner.nextLine();
                    String[] info = fileString.split(",");
                    Object[] row = {info[0],info[1]};
                    model.addRow(row);
     
                }
                
               
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            } 
           
            
        }
    }//GEN-LAST:event_loadcsvMouseClicked

